// Helper component for animated sections (copied from PublicAboutPage.js for consistency)
const AnimatedSection = ({ children, animationClass = "fade-in-up-on-scroll", threshold = 0.1, style = {} }) => {
    const sectionRef = useRef(null);

    useEffect(() => {
        const currentRef = sectionRef.current; 
        if (!currentRef) return;

        const observer = new IntersectionObserver(
            (entries) => {
                entries.forEach((entry) => {
                    if (entry.isIntersecting) {
                        currentRef.classList.add('is-visible');
                        observer.unobserve(currentRef);
                    }
                });
            },
            { threshold: threshold }
        );

        observer.observe(currentRef);

        return () => {
            if (currentRef) {
                observer.unobserve(currentRef);
            }
        };
    }, [threshold]);

    return (
        <div ref={sectionRef} className={`about-section-animate ${animationClass}`} style={style}>
            {children}
        </div>
    );
};

